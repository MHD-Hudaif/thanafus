
<?php

require_once __DIR__ . '/../../config/auth.php';

require_login();

$musabaqa_pdo =
    $GLOBALS['musabaqa_pdo'];

$id =
    (int)($_GET['id'] ?? 0);

/*
|--------------------------------------------------------------------------
| VALIDATE ID
|--------------------------------------------------------------------------
*/

if($id <= 0){

    header(
        'Location: '
        . APP_URL
        . '/admin/events'
    );

    exit;
}

/*
|--------------------------------------------------------------------------
| FIND EVENT
|--------------------------------------------------------------------------
*/

$stmt = $musabaqa_pdo->prepare("
    SELECT id
    FROM musabaqa_events
    WHERE id = ?
    LIMIT 1
");

$stmt->execute([$id]);

$event =
    $stmt->fetch(PDO::FETCH_ASSOC);

/*
|--------------------------------------------------------------------------
| INVALID EVENT
|--------------------------------------------------------------------------
*/

if(!$event){

    header(
        'Location: '
        . APP_URL
        . '/admin/events'
    );

    exit;
}

/*
|--------------------------------------------------------------------------
| SET ACTIVE EVENT
|--------------------------------------------------------------------------
*/

$_SESSION['active_event_id'] = $id;

/*
|--------------------------------------------------------------------------
| RESET ACTIVE TEAM
|--------------------------------------------------------------------------
*/

unset($_SESSION['active_team_id']);

/*
|--------------------------------------------------------------------------
| REDIRECT
|--------------------------------------------------------------------------
*/

header(
    'Location: '
    . APP_URL
    . '/admin/dashboard'
);

exit;

